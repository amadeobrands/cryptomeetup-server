public class PortalBean {

    private String id;

    private String creator;

    private String parent;

    private String creator_fee;

    private String ref_fee;

    private String k;

    private String price;

    private String st;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCreator() {
        return creator;
    }

    public void setCreator(String creator) {
        this.creator = creator;
    }

    public String getParent() {
        return parent;
    }

    public void setParent(String parent) {
        this.parent = parent;
    }

    public String getCreator_fee() {
        return creator_fee;
    }

    public void setCreator_fee(String creator_fee) {
        this.creator_fee = creator_fee;
    }

    public String getRef_fee() {
        return ref_fee;
    }

    public void setRef_fee(String ref_fee) {
        this.ref_fee = ref_fee;
    }

    public String getK() {
        return k;
    }

    public void setK(String k) {
        this.k = k;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getSt() {
        return st;
    }

    public void setSt(String st) {
        this.st = st;
    }
}
